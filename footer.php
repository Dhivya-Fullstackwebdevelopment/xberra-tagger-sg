    // FAQ toggle function
    window.toggleFAQ = function(event, btn) {
        event.preventDefault();
        event.stopPropagation();

        const answer = btn.nextElementSibling;
        const isOpen = btn.classList.contains('open');

        document.querySelectorAll('.faq-question.open').forEach(b => {
            if (b !== btn) {
                b.classList.remove('open');
                if (b.nextElementSibling) {
                    b.nextElementSibling.classList.remove('open');
                }
            }
        });

        if (!isOpen) {
            btn.classList.add('open');
            if (answer) {
                answer.classList.add('open');
            }
        } else {
            btn.classList.remove('open');
            if (answer) {
                answer.classList.remove('open');
            }
        }
    };
</script>


<script>
    document.addEventListener("DOMContentLoaded", function() {
        // Get ALL forms and use the first one (the one in the support-section)
        const allForms = document.querySelectorAll("form.support-form");

        // If no forms found, exit
        if (allForms.length === 0) {
            console.log("No forms found");
            return;
        }

        // Use the first form (the one in the support-section)
        const form = allForms[0];
        // console.log("Form found:", form); // Debug log

        // Get form elements from THIS specific form
        const inputs = form.querySelectorAll("input[required], textarea[required]");
        const emailInput = form.querySelector('input[type="email"]');
        const phoneInput = form.querySelector('input[name="phone"]');

        // Get captcha elements (these are outside the form but we need them)
        const captchaBox = document.getElementById("captchaBox");
        const refreshBtn = document.getElementById("refreshCaptcha");
        const captchaInput = document.getElementById("captchaInput");

        // Remove any existing formResult div from the wrong form
        const existingResults = document.querySelectorAll("#formResult");
        existingResults.forEach(el => el.remove());

        // Create a NEW form result div for THIS form
        const formResult = document.createElement("div");
        formResult.id = "formResult";
        formResult.className = "form-result mt-3 text-center";
        formResult.style.display = "none"; // Hidden by default
        formResult.style.padding = "15px";
        formResult.style.borderRadius = "5px";
        formResult.style.marginTop = "20px";

        // Append it to THIS form
        form.appendChild(formResult);

        let currentCaptcha = "";

        /* ---------------- CAPTCHA ---------------- */
        function generateCaptcha() {
            const chars = "ABCDEFGHJKLMNPQRSTUVWXYZ23456789";
            currentCaptcha = "";

            for (let i = 0; i < 6; i++) {
                currentCaptcha += chars.charAt(Math.floor(Math.random() * chars.length));
            }

            displayCaptcha();
        }

        function displayCaptcha() {
            if (!captchaBox) return;

            captchaBox.innerHTML = "";

            for (let char of currentCaptcha) {
                const span = document.createElement("span");
                span.textContent = char;
                span.style.display = "inline-block";
                span.style.margin = "0 3px";
                span.style.fontSize = "24px";
                span.style.fontWeight = "bold";
                span.style.transform = `rotate(${Math.random() * 30 - 15}deg)`;
                captchaBox.appendChild(span);
            }
        }

        if (refreshBtn) {
            refreshBtn.addEventListener("click", function(e) {
                e.preventDefault();
                generateCaptcha();
                if (captchaInput) captchaInput.value = "";
                removeError(captchaInput);
            });
        }

        generateCaptcha();

        /* ---------------- VALIDATION FUNCTIONS ---------------- */
        function showError(input, message) {
            if (!input) return;

            removeError(input);

            const error = document.createElement("div");
            error.className = "error-message";
            error.innerText = message;
            error.style.color = "#e5252a";
            error.style.fontSize = "12px";
            error.style.marginTop = "5px";

            input.parentElement.appendChild(error);
            input.classList.add("error-input");
            input.style.borderColor = "#e5252a";
        }

        function removeError(input) {
            if (!input || !input.parentElement) return;

            const existingError = input.parentElement.querySelector(".error-message");
            if (existingError) existingError.remove();
            input.classList.remove("error-input");
            input.style.borderColor = "";
        }

        // Remove error while typing
        inputs.forEach(input => {
            if (input) {
                input.addEventListener("input", function() {
                    removeError(input);
                    if (formResult) {
                        formResult.style.display = "none";
                        formResult.innerHTML = "";
                    }
                });
            }
        });

        // Clear captcha error when typing
        if (captchaInput) {
            captchaInput.addEventListener("input", function() {
                removeError(captchaInput);
            });
        }

        /* ---------------- FORM SUBMISSION ---------------- */
        form.addEventListener("submit", async function(e) {
            e.preventDefault();

            let isValid = true;

            // Hide any previous result
            if (formResult) {
                formResult.style.display = "none";
                formResult.innerHTML = "";
            }

            /* ---------- REQUIRED VALIDATION ---------- */
            inputs.forEach(input => {
                if (input && input.value.trim() === "") {
                    const label = input.closest(".form-group")?.querySelector("label");
                    let fieldName = "This field";

                    if (label) {
                        fieldName = label.innerText.replace("*", "").trim();
                    }

                    showError(input, fieldName + " is required");
                    isValid = false;
                }
            });

            /* ---------- EMAIL VALIDATION ---------- */
            if (emailInput && emailInput.value.trim() !== "") {
                const emailPattern = /^[^ ]+@[^ ]+\.[a-z]{2,}$/i;
                if (!emailPattern.test(emailInput.value.trim())) {
                    showError(emailInput, "Enter valid Email Address");
                    isValid = false;
                }
            }

            /* ---------- PHONE VALIDATION ---------- */
            if (phoneInput && phoneInput.value.trim() !== "") {
                const phonePattern = /^[0-9+\s]{8,15}$/;
                if (!phonePattern.test(phoneInput.value.trim())) {
                    showError(phoneInput, "Enter valid Phone Number");
                    isValid = false;
                }
            }

            /* ---------- CAPTCHA VALIDATION ---------- */
            if (captchaInput) {
                if (captchaInput.value.trim() === "") {
                    showError(captchaInput, "Simple Captcha is required");
                    isValid = false;
                } else if (captchaInput.value.trim().toUpperCase() !== currentCaptcha) {
                    showError(captchaInput, "Incorrect Captcha");
                    generateCaptcha();
                    captchaInput.value = "";
                    isValid = false;
                }
            }

            if (!isValid) return;

            /* ---------- SUBMIT TO PHP FILES ---------- */
            try {
                // Show loading state
                const submitBtn = form.querySelector('button[type="submit"]');
                if (submitBtn) {
                    submitBtn.disabled = true;
                    submitBtn.innerText = "Sending...";
                }

                // Create FormData object
                const formData = new FormData(form);

                // Add captcha value to FormData
                // if (captchaInput) {
                //     formData.set('captcha', captchaInput.value);


                //     // SAVE TO DATABASE
                //     const dbResponse = await fetch('save_contact.php', {
                //         method: 'POST',
                //         body: formData
                //     });

                //     const dbResult = await dbResponse.json();

                //     if (dbResult.status !== 'success') {
                //         throw new Error('Database save failed');
                //     }
                // }

                // 1. Send to admin (email_sender.php)
                const senderResponse = await fetch('email_sender', {
                    method: 'POST',
                    body: formData
                });

                const senderResult = await senderResponse.json();

                if (senderResult.status !== 'success') {
                    throw new Error(senderResult.message || 'Failed to send enquiry');
                }

                // 2. Send auto-reply to user (email_receiver.php)
                const receiverResponse = await fetch('email_receiver', {
                    method: 'POST',
                    body: formData
                });

                const receiverResult = await receiverResponse.json();

                // Show success message
                if (formResult) {
                    formResult.style.display = "block";
                    formResult.style.backgroundColor = "#d4edda";
                    formResult.style.color = "#155724";
                    formResult.style.border = "1px solid #c3e6cb";
                    formResult.innerHTML = "✅ Thank you! Your enquiry has been sent successfully. Our team will contact you within 24-48 hours.";

                    // Scroll to success message
                    formResult.scrollIntoView({
                        behavior: 'smooth',
                        block: 'center'
                    });
                }



                // Reset form
                form.reset();
                generateCaptcha();

            } catch (error) {
                console.error('Error:', error);

                // Show error message
                if (formResult) {
                    formResult.style.display = "block";
                    formResult.style.backgroundColor = "#f8d7da";
                    formResult.style.color = "#721c24";
                    formResult.style.border = "1px solid #f5c6cb";
                    formResult.innerHTML = "❌ Sorry, something went wrong. Please try again or contact us directly.";
                }

            } finally {
                // Reset button state
                const submitBtn = form.querySelector('button[type="submit"]');
                if (submitBtn) {
                    submitBtn.disabled = false;
                    submitBtn.innerText = "Submit";
                }
            }
        });
    });
</script>

